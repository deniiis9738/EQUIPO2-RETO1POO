public class Cotization {
    public int gCot;
    public int dinero;

    public Cotization(int gCot, int dinero) {
        this.gCot = gCot;
        this.dinero = dinero;
    }

    public int getGCot() {
        return gCot;
    }
    public int getDinero() {
        return dinero;
    }
}